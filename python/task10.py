def main():
    # todo: 10.1 Дано целое число A. Проверить истинность высказывания: «Число A является четным».
    # todo: 10.2 Дано целое число A. Проверить истинность высказывания: «Число A является нечетным».
    # Примечание: В задании  требуется вывести логическое значение True, если выражение для введеных исходных данных является истинным, и значение False в противном случае.
    
    try:
        # Считываем целое число
        A = int(input("Введите целое число A: "))
        
        # Проверяем, является ли число четным
        is_even = A % 2 == 0
        
        # Проверяем, является ли число нечетным
        is_odd = not is_even
        
        # Выводим результаты
        print(f"\nРезультаты:")
        print(f"Число {A} является четным: {is_even}")
        print(f"Число {A} является нечетным: {is_odd}")
        
    except ValueError:
        print("Ошибка: введите корректное целое число")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

if __name__ == "__main__":
    main()