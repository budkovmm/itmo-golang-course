def main():
    # todo: Решить линейное уравнение A·x + B = 0, заданное своими коэффициентами A и B (коэффициент A не равен 0).
    # Примечание: коэффициенты получаем через функцию input().
    
    try:
        # Считываем коэффициенты
        A = float(input("Введите коэффициент A: "))
        B = float(input("Введите коэффициент B: "))
        
        # Проверяем, что A не равен 0
        if A == 0:
            print("Ошибка: коэффициент A не может быть равен 0")
            return
        
        # Решаем уравнение: A·x + B = 0 => x = -B/A
        x = -B / A
        
        # Выводим результат
        print(f"\nРешение уравнения {A}·x + {B} = 0:")
        print(f"x = {x}")
        
    except ValueError:
        print("Ошибка: введите корректные числа")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

if __name__ == "__main__":
    main()
