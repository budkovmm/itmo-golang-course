def main():
    #todo: Дана сторона квадрата a. Найти его площадь S = a²
    # Примечание: сторону квадрата получаем через функцию input().
    
    try:
        # Считываем длину стороны квадрата
        a = float(input("Введите длину стороны квадрата: "))
        
        # Проверяем, что сторона положительная
        if a <= 0:
            print("Ошибка: длина стороны должна быть положительным числом")
            return
        
        # Вычисляем площадь квадрата
        S = a ** 2
        
        # Выводим результат
        print(f"Площадь квадрата со стороной {a} равна {S}")
        
    except ValueError:
        print("Ошибка: введите корректное число")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

if __name__ == "__main__":
    main()
